package new_project;
 
public class Customer {
 
    private String cusId;
    private String cusName;
    private String cus_accnum;
    private String cus_accbal;
    private String cus_balcur;
    
 
    public String getCusId() {
        return cusId;
    }
 
    public void setCusId(String cusId) {
        this.cusId = cusId;
    }
 
    public String getCusName() {
        return cusName;
    }
 
    public void setCusName(String cusName) {
        this.cusName = cusName;
    }
    public String getCus_accnum() {
        return cus_accnum;
    }
 
    public void setCus_accnum(String cus_accnum) {
        this.cus_accnum = cus_accnum;
    }
    public String getCus_accbal() {
        return cus_accbal;
    }
 
    public void setCus_accbal(String cus_accbal) {
        this.cus_accbal = cus_accbal;
    }
    public String getCus_balcur() {
        return cus_balcur;
    }
 
    public void setCus_balcur(String cus_balcur) {
        this.cus_balcur = cus_balcur;
    }
 
    
 
}